export function seedData() {
  return fakeData.map((album) => {
    const date = randomDate()
    const year = date.getFullYear().toFixed(0)
    return { ...album, releaseDate: year }
  })  
}

function randomDate() {
  const from = new Date(1940, 0, 1)
  const to = new Date()
  return new Date(
    from.getTime() + Math.random() * (to.getTime() - from.getTime())
  )
}
